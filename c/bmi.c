#include "stdio.h"
#include "string.h"

float indx_bmi(char sex, float h, float w){
    printf("Calculate BMI for: %c, %.1f cm, %.1f kg\n",sex,h,w);
    return ((w/(h*h)*10000));
}

int main (){
    char sex;
    float up_limit, height, weight;
    
    printf("Enter you sex (M or F): ");
    scanf("%c", &sex);
    
    printf("Enter you height in cm: ");
    scanf("%f", &height);
    
    printf("Enter you weight in kg: ");
    scanf("%f", &weight);
    float bmi = indx_bmi(sex,height,weight);

    printf("You BMI is %.1f\n",bmi);

    if (!strcmp(&sex,"M")){
        up_limit=24;
    }
    else{
        up_limit=25;
    }    

    if (bmi<18.5){
        printf("You are so long!\n");
    }
    else if (bmi>up_limit){
        printf("You are short!\n");
    }
    else{
        printf("You absolutly normal\n");
    }
    
}